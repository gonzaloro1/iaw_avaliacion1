numero1=int(input("\nDame numero1: "))
numero2=int(input("Dame numero2: "))
print ("\n%d + %d = %d" % (numero1,numero2, numero1+numero2))
print ("%d - %d = %d" % (numero1,numero2, numero1-numero2))
print ("%d * %d = %d" % (numero1,numero2, numero1*numero2))
print ("%d / %d = %d" % (numero1,numero2, numero1/numero2))
print ("%d / %d = %.3f\n" % (numero1,numero2, numero1/numero2))
